/******************************************************************************

                            4.1有條件的執行(if述句)

*******************************************************************************/

#include <stdio.h>

int main(){
    int a;
    printf("a=");
    scanf("%d",&a); //使用者輸入數字,將數字轉換%d型態,指定給a變數空間位置
    if(a>4){
        printf("a is larger than 4 .\n");
    }else if(a<4){
        printf("a is smaller than 4. \n");
    }else{
        printf("a is 4. \n");
    }
    return 0;
}